
package javabeginer;

public class AssignmentOperator {
    public static void main(String[] args) {
       
        int x=6,y=2;
        x+=y;      //x=x+y=8
        System.out.println("x= "+x);
        
         x-=y;      //x=x-y=4
        System.out.println("x= "+x);
        
         x*=y;      //x=x*y
                   //=12
        System.out.println("x= "+x);
        
         x/=y;      //x=x/y=3
        System.out.println("x= "+x);
        
         x%=y;      //x=x%y=0
        System.out.println("x= "+x);
        
        
        
    }
}
