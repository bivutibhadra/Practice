
package javabeginer;

public class UsePrintf {
     public static void main(String[] args) {
        boolean b=true ;    //Dinamic initilazion...
        boolean h =false;
        char c='m';
        short s=32677;
        int i=126587;
        float f=10.2f;
        double d=10.2;
        
        
         System.out.printf("boolean= %b\n",b);
         System.out.printf("boolean= %b\n",h);
         System.out.printf("char= %c\n",c);
         System.out.printf("short= %s\n",s);
         System.out.printf("int= %d\n",i);
         System.out.printf("float= %f\n",f);
         System.out.printf("double= %f\n",d);
     }
}
